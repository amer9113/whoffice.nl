            <h4 class="cnt_heads w3-red">5.8  Een bol-opleiding en een bbl-opleiding; wat is het verschil? </h4>
            <p>Bbl staat voor beroepsbegeleidende leerweg. Bij een bbl-opleiding combineer je leren en werken, wat betekent dat je meestal 1 dag per week naar school gaat en 4 dagen per week gaat werken bij een leerbedrijf in de sector waar jij voor gekozen hebt. Bbl-opleidingen zijn erg geschikt voor beroepen waarbij de praktijk erg belangrijk is, zoals bijvoorbeeld het beroep van schilder of timmerman. Daarnaast is het zeer geschikt voor studenten die liever meteen aan de slag gaan in plaats van uren in de schoolbanken zitten. Als je een bbl-opleiding volgt, heb je geen recht op studiefinanciering. Dit komt doordat je met 4 dagen werk vaak voldoende geld verdient om jezelf te onderhouden. </p>
            <p>Bol staat voor beroepsopleidende leerweg. Bij een bol-opleiding zit je het grootste deel van de week op school. Het is een meer theoretische opleiding dan bbl. Je loopt wel stage om ervaring op te doen in de praktijk.  </p>
