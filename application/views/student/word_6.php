            <h4 class="cnt_heads w3-red">6.16 Tien tips voor het goed gebruiken van je netwerk</h4>
            <ol>
                <li>Netwerk het liefst met mensen met wie je het goed kunt vinden. Dit zorgt er ook voor dat je elkaar begrijpt in wat je nodig hebt. Bovendien is een goede netwerkrelatie gebaseerd op wederzijds vertrouwen. Wees dan ook open en oprecht in je interesse in anderen: mensen merken meteen als je een praatje komt maken met een verborgen agenda ( dit betekent dat je alleen maar aardig doet om iets van de ander te krijgen). </li>
                <li>Verzamel visitekaartjes en berg ze gesystematiseerd op (in een apart doosje op bijvoorbeeld alfabet of beroep gesorteerd). Schrijf bij het kaartje waar je diegene hebt ontmoet, en wat jullie eventueel voor elkaar kunnen betekenen.</li>
                <li>Als je weet wat je wilt en daar met anderen over praat, komen informatie en contacten van je netwerkpartner ( degene met wie je praat) vaak als vanzelf naar boven. </li>
                <li>Houd er ook tijdens een gesprek op jouw verzoek rekening mee dat netwerken altijd wederzijds is. Netwerken begint vaak met het meedenken met een ander, oftewel: je begint meestal met geven voordat je gaat nemen.</li>
                <li>Zorg ervoor dat je een goede naam hebt en houdt. Verwijs bijvoorbeeld geen mensen door naar jouw contacten als je twijfelt aan hun motieven (of zij wel eerlijk zijn in het willen van dit contact).</li>
                <li>Denk bij netwerken ook aan de lange termijn (niet voor nu direct maar waarschijnlijk in de toekomst). Misschien kun je nu nog niets met een contact, over een half jaar blijk je ineens een vraag te hebben waar dit contact je mee kan helpen.</li>
                <li>Bedank mensen die iets voor je hebben gedaan de volgende dag nog even met een mailtje, en vertel wat je eraan gehad hebt.</li>
                <li>Krijg je een uitnodiging waarop je niet kunt ingaan, laat dat even weten. Zo houd je je contacten warm. Soms vind je vanuit je cultuur dat je een uitnodiging niet kunt weigeren en daarom geef je geen antwoord; dat vinden mensen in Nederland niet netjes en fijn; zij willen altijd een antwoord hebben). Je kunt bijvoorbeeld zeggen: “het spijt mij heel erg maar ik kan jammer genoeg niet komen; ik wens je of jullie een fijn samenzijn toe. </li>
                <li>Waardeer het als je van iemand een tip krijgt, ook al kun je er zelf niets mee. Misschien kun je een ander er blij mee maken. Vertel dat ook aan de tipgever. "Het sloot niet aan bij mijn ervaring, maar ik denk dat een vriendin/vriend daarvoor heel erg  geschikt zou zijn. Is het goed als ik dit aan haar/hem doorgeef?"</li>
                <li>Kun je goed met iemand opschieten dan stuur je een kaartje met verjaardagen en laat ook eens iets van je horen als je hem of haar niet nodig hebt.</li>
            </ol>
